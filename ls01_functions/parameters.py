def square(x: float) -> float:
    """This function returns the square of the input parameter.

    Args:
        x (float): Input parameter.

    Returns:
        float: Square of the input parameter."""
    return x**2


def power(x: float, y: float) -> float:
    """This function returns the power of the input parameters.

    Args:
        x (float): Base.
        y (float): Exponent.

    Returns:
        float: Power of the input parameters."""
    return x**y
